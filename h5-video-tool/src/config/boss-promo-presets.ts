export interface BossPresetShot {
  duration: number;
  prompt: string;
}

export interface BossPreset {
  id: string;
  name: string;
  description: string;
  shots: BossPresetShot[];
}

/** 从用户创意中提取 BOSS 名称，用于本地即时生成分镜的个性化 */
export function extractBossNameFromPrompt(prompt: string): string {
  const t = prompt.trim();
  if (!t) return 'BOSS';
  // 优先匹配中文名（2-6字）
  const zh = t.match(/[\u4e00-\u9fa5]{2,6}/);
  if (zh) return zh[0];
  // 英文单词
  const en = t.match(/[a-zA-Z]{3,20}/);
  if (en) return en[0];
  return 'BOSS';
}

/** 预设中可替换的主体名称（骷髅国王/骷髅王/BOSS → 用户输入的主体如浪人、骷髅王） */
const SUBJECT_REPLACE_REGEX = /骷髅国王|骷髅王|BOSS/g;

/** 将任意预设的 shots 按用户主体名称个性化（宣传骷髅王→骷髅王，宣传浪人→浪人） */
export function personalizePresetShots(preset: BossPreset, subjectPrompt: string): BossPresetShot[] {
  const subjectName = extractBossNameFromPrompt(subjectPrompt);
  return preset.shots.map((s) => ({
    duration: s.duration,
    prompt: s.prompt.replace(SUBJECT_REPLACE_REGEX, subjectName),
  }));
}

/** 根据用户模糊 prompt 即时生成 3 镜 × 5s 分镜（无需 API），体验与 CG trailer 一致 */
export function createLocalBossStoryboard(userPrompt: string): BossPresetShot[] {
  return personalizePresetShots(BOSS_PRESETS[0], userPrompt);
}

